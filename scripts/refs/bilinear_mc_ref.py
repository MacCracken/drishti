# ---------------------------------------------------------------------------
# Spec primitives (04.conventions.md). Python's >> on ints is ARITHMETIC, which
# is what the spec means by >> (sign-propagating) and what Cyrius spells >>>.
# ---------------------------------------------------------------------------

def round2(x, n):
    if n == 0:
        return x
    return (x + (1 << (n - 1))) >> n


def clip3(lo, hi, v):
    return lo if v < lo else (hi if v > hi else v)


def clip1(v, bd):
    return clip3(0, (1 << bd) - 1, v)


def inter_rounds(bd, is_compound):
    """Spec 7.11.3.2, rounding variables derivation process (quoted at :2889-2897)."""
    r0 = 3
    r1 = 7 if is_compound else 11
    if bd == 12:
        r0 += 2
        if not is_compound:
            r1 -= 2
    post = 2 * 7 - (r0 + r1)          # 2 * FILTER_BITS - (InterRound0 + InterRound1)
    return r0, r1, post


def filter_idx_spec(interp_filter, dim):
    """Spec 7.11.3.4 set selection -> a SPEC set index."""
    f = interp_filter
    if dim <= 4:
        if f == 0 or f == 2:          # EIGHTTAP, EIGHTTAP_SHARP
            f = 4
        elif f == 1:                  # EIGHTTAP_SMOOTH
            f = 5
        # BILINEAR (3): no else branch — unchanged at every extent.
    return f


def filter_idx_drishti(interp_filter, dim):
    """The same rule expressed in drishti's permuted index space."""
    return PERM[filter_idx_spec(interp_filter, dim)]


# ---------------------------------------------------------------------------
# The unscaled geometry reduction of 7.11.3.3, and 7.11.3.4 over it.
# ---------------------------------------------------------------------------

def pos16(x, mv, sub):
    """1/16-pel position of a block edge. mv is 1/8 LUMA pel; sub the plane subsampling.

    For equal reference and current dimensions the full 7.11.3.3 scaling collapses exactly
    to this: xScale == 1 << REF_SCALE_SHIFT, stepX == 1024, and with
    startX = (pos16 << 6) + 32 every sample position p = startX + 1024*c satisfies
        (p >> 10) == (pos16 >> 4) + c        and      (p >> 6) & SUBPEL_MASK == pos16 & 15
    so the halfSample / off rounding terms cancel at both extractions.
    """
    return (x << 4) + ((2 * mv) >> sub)


def fill_val(p, r, c, bd):
    """The shared deterministic plane fill — matches mcd_val() in tests/av1_mc_driver.tcyr."""
    return ((r * 31) + (c * 17) + (((r * c) % 11) * 5) + (p * 29)) % (1 << bd)


def make_plane(p, pw, ph, bd):
    return [[fill_val(p, r, c, bd) for c in range(pw)] for r in range(ph)]


def pred_block(plane, pw, ph, x, y, w, h, mvr, mvc, fx, fy, bd, sub_x, sub_y,
               is_compound=0):
    """Spec 7.11.3.4 over the unscaled geometry. Returns list[h][w]."""
    r0, r1, _post = inter_rounds(bd, is_compound)
    lastX, lastY = pw - 1, ph - 1

    px = pos16(x, mvc, sub_x)
    py = pos16(y, mvr, sub_y)
    ix, mx = px >> 4, px & 15
    iy, my = py >> 4, py & 15

    # Horizontal pass: the H filter is selected by the block WIDTH.
    hset = filter_idx_spec(fx, w)
    ftab_h = SPEC_SUBPEL_FILTERS[hset][mx]
    inter = [[0] * w for _ in range(h + 7)]
    for r in range(h + 7):
        sy = clip3(0, lastY, iy + r - 3)
        for c in range(w):
            s = 0
            for t in range(8):
                sx = clip3(0, lastX, ix + c + t - 3)
                s += ftab_h[t] * plane[sy][sx]
            inter[r][c] = round2(s, r0)

    # Vertical pass: the V filter is selected by the block HEIGHT.
    vset = filter_idx_spec(fy, h)
    ftab_v = SPEC_SUBPEL_FILTERS[vset][my]
    out = [[0] * w for _ in range(h)]
    for r in range(h):
        for c in range(w):
            s = 0
            for t in range(8):
                s += ftab_v[t] * inter[r + t][c]
            v = round2(s, r1)
            out[r][c] = v if is_compound else clip1(v, bd)
    return out


def checksum(block):
    """Position-weighted checksum sum v*(i+1) — matches mcd_cs() in the .tcyr suites."""
    ck = 0
    i = 0
    for row in block:
        for v in row:
            ck += (i + 1) * v
            i += 1
    return ck


def run_case(fw, fh, planes, subx, suby, bd, plane_idx, x, y, w, h, mvr, mvc, fx, fy):
    sx = subx if plane_idx > 0 else 0
    sy = suby if plane_idx > 0 else 0
    pw = (fw + subx) >> subx if plane_idx > 0 else fw
    ph = (fh + suby) >> suby if plane_idx > 0 else fh
    plane = make_plane(plane_idx, pw, ph, bd)
    out = pred_block(plane, pw, ph, x, y, w, h, mvr, mvc, fx, fy, bd, sx, sy)
    return checksum(out), out[0][0], out[h - 1][w - 1]


def verify_against_spec(path):
    """Re-derive Subpel_Filters from the spec markdown and check the embedded copy.

    The embedded table must never be trusted on its own — a hand-typed 8-tuple is exactly
    the failure the 0.7.95 warp-table bite was built to prevent, which is why this one was
    machine-generated. Pass the fetched 08.decoding.process.md.
    """
    import hashlib
    import re

    raw = open(path, "rb").read()
    got = hashlib.md5(raw).hexdigest()
    assert got == SPEC_MD5, "spec md5 drift: %s != %s" % (got, SPEC_MD5)

    txt = raw.decode()
    i = txt.index("where the constant array Subpel_Filters is specified as:")
    blk = txt[i:i + 9000]
    s = blk.index("{")
    e = blk.index("~~~~~", s)
    vals = [int(v) for v in re.findall(r"-?\d+", blk[s:e])]
    assert len(vals) == 768, "expected 768 coefficients, got %d" % len(vals)
    spec = [[vals[(st * 16 + ph) * 8:(st * 16 + ph) * 8 + 8] for ph in range(16)]
            for st in range(6)]
    assert spec == SPEC_SUBPEL_FILTERS, "embedded table != spec"

    for st in range(6):
        assert spec[st][0] == [0, 0, 0, 128, 0, 0, 0, 0], "phase-0 identity (set %d)" % st
        assert all(sum(r) == 128 for r in spec[st]), "row sums (set %d)" % st
        assert all(v % 2 == 0 for r in spec[st] for v in r), "taps even (set %d)" % st

    bil = spec[SPEC_BILINEAR_SET]
    for p, row in enumerate(bil):
        nz = sum(1 for v in row if v != 0)
        assert nz == (1 if p == 0 else 2), "BILINEAR phase %d is not 2-tap: %s" % (p, row)
    return {"md5": got}


def drishti_rows(spec_set):
    """The halved 15-phase rows drishti holds for a given SPEC set (phase 0 dropped)."""
    return [[v // 2 for v in SPEC_SUBPEL_FILTERS[spec_set][p]] for p in range(1, 16)]


