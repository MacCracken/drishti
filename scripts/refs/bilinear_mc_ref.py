# BILINEAR interpolation filter (spec 7.11.3.4) and, as its own validation, the whole
# unscaled single-reference translation MC path that drishti's av1_mc_pred_block implements.
# The oracle for the BILINEAR known answers; never back-derived from the Cyrius.
#
# THIS PORT IS DELIBERATELY IN THE **SPEC** CONVENTION, NOT DRISHTI'S.
#
# drishti carries dav1d's table: 15 phases, rows summing to 64, with every kernel round one
# bit SMALLER. The spec's is 16 phases, rows summing to 128. Writing the oracle in the spec
# convention makes it a genuine CROSS-CONVENTION check: if the two agree pixel-for-pixel,
# the halving-plus-reduced-shift equivalence is PROVEN by the test rather than assumed by a
# comment. The equivalence rests on two facts, both checked here rather than asserted:
#   (1) Round2(2*s, n) == Round2(s, n-1) for an ARITHMETIC shift, and
#   (2) every spec tap is even (verify_against_spec asserts it over all 768 values).
#
# SET INDEX PERMUTATION (spec -> dav1d/drishti), asserted by verify_against_spec():
#   spec 0 REGULAR   -> drishti 0        spec 3 BILINEAR    -> drishti 5
#   spec 1 SMOOTH    -> drishti 1        spec 4 REGULAR/4   -> drishti 3
#   spec 2 SHARP     -> drishti 2        spec 5 SMOOTH/4    -> drishti 4
#
# THE SELECTION RULE (spec 7.11.3.4, quoted verbatim from the fetched spec):
#     interpFilter = InterpFilters[ candRow ][ candCol ][ 1 ]
#     if ( w <= 4 ) {
#         if ( interpFilter == EIGHTTAP || interpFilter == EIGHTTAP_SHARP ) {
#             interpFilter = 4
#         } else if ( interpFilter == EIGHTTAP_SMOOTH ) {
#             interpFilter = 5
#         }
#     }
